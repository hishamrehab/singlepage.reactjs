const sectionCenter = document.querySelector(".section-center");
const container = document.querySelector(".btn-container");
// load items
window.addEventListener("DOMContentLoaded", function () {
  displayMenuItems(menu);
  displayMenButtons();
});
function displayMenuItems(menuItems) {
  let diplayMenu = menuItems.map(function (item) {
    return `<article class="menu-item">
            <img src=${item.img}  class="photo" alt=${item.title}>
            <div class="item-info">
                <header>
                <span class="text">${item.title}</span>
                <h4 class="price">${item.price}$</h4>
                <div class="underline"></div>
                </header>
               <p class="item-text">
               ${item.desc}
               </p>
            </div>
        </article>`;
  });
  diplayMenu = diplayMenu.join("");
  sectionCenter.innerHTML = diplayMenu;
}
function displayMenButtons() {
  const catigories = menu.reduce(
    function (values, item) {
      if (!values.includes(item.category)) {
        values.push(item.category);
      }
      return values;
    },
    ["all"]
  );
  const categoryBtns = catigories.map(function (category) {
      return `<button class="filter-btn" type="button" 
      data-id="${category}">${category}
      </button>`
  })
  .join("");
  container.innerHTML = categoryBtns;
  const filterBtns = document.querySelectorAll(".filter-btn");
  // filter  items
  filterBtns.forEach(function (btn) {
    btn.addEventListener("click", function (e) {
      category = e.currentTarget.dataset.id;
      const menuCategory = menu.filter(function (menuItem) {
        if (menuItem.category === category) {
          return menuItem;
        }
      });
      if (category === "all") {
        displayMenuItems(menu);
      } else {
        displayMenuItems(menuCategory);
      }
    });
  });
}
